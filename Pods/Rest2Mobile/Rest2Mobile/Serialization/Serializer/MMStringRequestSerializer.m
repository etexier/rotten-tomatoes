#import "MMStringRequestSerializer.h"
#import "MMAssert.h"


@implementation MMStringRequestSerializer

#pragma mark - AFURLRequestSerializer

- (NSURLRequest *)requestBySerializingRequest:(NSURLRequest *)request
                               withParameters:(id)parameters
                                        error:(NSError *__autoreleasing *)error
{
    MMParameterAssert(request);

    if ([self.HTTPMethodsEncodingParametersInURI containsObject:[[request HTTPMethod] uppercaseString]]) {
        return [super requestBySerializingRequest:request withParameters:parameters error:error];
    }

    NSMutableURLRequest *mutableRequest = [request mutableCopy];

    [self.HTTPRequestHeaders enumerateKeysAndObjectsUsingBlock:^(id field, id value, BOOL * __unused stop) {
        if (![request valueForHTTPHeaderField:field]) {
            [mutableRequest setValue:value forHTTPHeaderField:field];
        }
    }];

    NSString *charset = (__bridge NSString *)CFStringConvertEncodingToIANACharSetName(CFStringConvertNSStringEncodingToEncoding(NSUTF8StringEncoding));

    [mutableRequest setValue:[NSString stringWithFormat:@"text/plain; charset=%@", charset] forHTTPHeaderField:@"Content-Type"];

    // This is required for NSNumbers sent in
    if ([parameters respondsToSelector:@selector(stringValue)]) {
        parameters = [parameters stringValue];
    }

    MMAssert([parameters isKindOfClass:[NSString class]], @"%@ is not a string (NSString) object", [parameters class]);

    [mutableRequest setHTTPBody:[parameters dataUsingEncoding:NSUTF8StringEncoding]];

    return mutableRequest;
}

@end
